package com.ks.bean;

/**
 * 封装事务的  id和类型  的类
 */
public class Sort {
    private int sortId;
    private String sortName;

    public int getSortId() {
        return sortId;
    }

    public void setSortId(int sortId) {
        this.sortId = sortId;
    }

    public String getSortName() {
        return sortName;
    }

    public void setSortName(String sortName) {
        this.sortName = sortName;
    }
}
